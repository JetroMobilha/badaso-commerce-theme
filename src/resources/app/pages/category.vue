<script>
import CarouselSingle from './../components/carousel-single/carousel.vue'
import CarouselItemSingle from './../components/carousel-single/carousel-item.vue'
import Pagination from './../components/pagination/pagination.vue'
import CommerceProductAlt from '../components/commerce-product-alt.vue'
import { mapState } from 'vuex'
export default {
  components: {
    CarouselSingle,
    CarouselItemSingle,
    Pagination,
    CommerceProductAlt
  },
  data() {
    return {
      currentPage: 1,
      products: {
        data: []
      }
    }
  },
  computed: {
    ...mapState({
      productLimit(state) {
        return parseInt(this.$_.find(state.moduleConfigurations, { key: "homeProductLimit" }).value);
      },
    })
  },
  watch: {
    currentPage: {
      handler(val) {
        this.getProducts()
      }
    }
  },
  beforeRouteEnter(to, from, next) {
    next(vm => {
      document.title = `${vm.$voca.titleCase(vm.$route.params.slug, true)} Category - Badaso Commerce Theme`
    })
  },
  mounted() {
    this.getProducts()
  },
  methods: {
    getProducts() {
      this.$api.badasoProduct
        .browseByCategorySlug({
          slug: this.$route.params.slug,
          page: this.currentPage
        })
        .then(res => {
          this.products = res.data.products
        })
        .catch(err => {
          console.error(err);
        })
    },
  }
}
</script>